# Write a program to perform string operations (slicing, concatenation, reversing). 

s = "Aditya Vishwakarma"
print("Original string:", s)

print("Slicing [0:5]:", s[0:5])
print("Slicing [7:]", s[7:])
print("Slicing [-6:]:", s[-6:])

t = " Welcome!"
print("Concatenated:", s + t)

print("Reversed:", s[::-1])