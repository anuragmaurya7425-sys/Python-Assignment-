# Performing all arithmetic operation 
# + , - , * , / , %

# take two input from user
num1 = int(input("Enter first number :"))
num2 = int(input("Enter second number :"))

#Addition operation
Sum = num1 + num2
print(f"Sum of both the number is :{Sum}")

# Substarction 
Sub = num1 - num2
print(f"Substraction is :{Sub}")

# Multiplication 
Multi = num1 * num2
print(f"Multiplication is :{Multi}")

# Divition
Div = num1 / num2
print(f"Divition is :{Div}")

# Moduler
Mod = num1 % num2
print(f"Remender is :{Mod}")